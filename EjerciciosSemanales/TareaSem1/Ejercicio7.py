Valor1 = int(input("Ingrese el 1° valor: "))
Valor2 = int(input("Ingrese el 2° valor: "))

if Valor1 == Valor2:
    print("Los valores son iguales")
elif Valor1 != Valor2:
    print("Los valores son diferentes")
    if Valor1 > Valor2:
        print(f"El valor {Valor1} es mayor valor {Valor2} ")
    else:
        print(f"El valor {Valor1} es menor valor {Valor2} ")
else:
    ("Nn")

    

